def numeric(row: dict[str, Any], key: str, default: float = 0.0) -> float:
    value = row.get(key)
    if isinstance(value, (int, float)):
        return float(value)
    return default


def classification_metrics(rows: list[dict[str, Any]], threshold: float = 0.5) -> dict[str, Any]:
    if not rows:
        return {
            "n": 0,
            "coverage": 0,
            "accuracy": None,
            "precision_up": None,
            "recall_up": None,
            "f1_binary": None,
            "f1_macro": None,
            "pos_rate": None,
        }

    tp = fp = tn = fn = 0
    for row in rows:
        target = int(row["target"])
        pred = 1 if float(row["proba_up"]) >= threshold else 0
        if pred == 1 and target == 1:
            tp += 1
        elif pred == 1 and target == 0:
            fp += 1
        elif pred == 0 and target == 0:
            tn += 1
        else:
            fn += 1

    def safe_div(num: float, den: float) -> float | None:
        return num / den if den else None

    precision_up = safe_div(tp, tp + fp)
    recall_up = safe_div(tp, tp + fn)
    precision_down = safe_div(tn, tn + fn)
    recall_down = safe_div(tn, tn + fp)

    def f1(precision: float | None, recall: float | None) -> float | None:
        if precision is None or recall is None or precision + recall == 0:
            return None
        return 2 * precision * recall / (precision + recall)

    f1_up = f1(precision_up, recall_up)
    f1_down = f1(precision_down, recall_down)
    f1_values = [value for value in (f1_up, f1_down) if value is not None]
    n = len(rows)

    return {
        "n": n,
        "coverage": n,
        "accuracy": safe_div(tp + tn, n),
        "precision_up": precision_up,
        "recall_up": recall_up,
        "precision_down": precision_down,
        "recall_down": recall_down,
        "f1_binary": f1_up,
        "f1_macro": mean(f1_values) if f1_values else None,
        "pos_rate": safe_div(tp + fp, n),
        "tp": tp,
        "fp": fp,
        "tn": tn,
        "fn": fn,
    }


def dataset_summary(path: Path) -> dict[str, Any]:
    with path.open(newline="", encoding="utf-8") as fh:
        reader = csv.reader(fh)
        header = next(reader)
        rows = list(reader)
    dates = [row[0] for row in rows if row]
    return {
        "file": str(path.relative_to(ROOT)),
        "rows": len(rows),
        "columns": len(header),
        "date_min": min(dates) if dates else None,
        "date_max": max(dates) if dates else None,
    }


def feature_group(feature: str, leakage_rows: dict[str, dict[str, Any]]) -> str:
    if feature in leakage_rows:
        return str(leakage_rows[feature].get("group") or "Other")
    if "gdelt" in feature:
        return "GDELT"
    if "conflict" in feature or "fatal" in feature:
        return "Conflict"
    if "inventory" in feature or "production" in feature or "import" in feature:
        return "Supply"
    if "oil" in feature or "sp500" in feature or "vix" in feature or "usd" in feature:
        return "Market"
    if "month" in feature or "day_of_week" in feature:
        return "Calendar"
    if "yield" in feature or "rate" in feature or "cpi" in feature:
        return "Macro"
    return "Other"


