def sha256(path: Path) -> str:
    h = hashlib.sha256()
    with path.open("rb") as fh:
        for block in iter(lambda: fh.read(1024 * 1024), b""):
            h.update(block)
    return h.hexdigest()


def convert(value: str) -> Any:
    value = value.strip()
    if value == "":
        return None
    if value.lower() in {"true", "false"}:
        return value.lower() == "true"
    if re.fullmatch(r"-?\d+", value):
        try:
            return int(value)
        except ValueError:
            return value
    if re.fullmatch(r"-?(\d+(\.\d*)?|\.\d+)(e-?\d+)?", value, re.IGNORECASE):
        try:
            parsed = float(value)
            if math.isfinite(parsed):
                return parsed
        except ValueError:
            return value
    return value


def read_csv(path: Path, limit: int | None = None) -> list[dict[str, Any]]:
    with path.open(newline="", encoding="utf-8") as fh:
        rows = [{key: convert(value) for key, value in row.items()} for row in csv.DictReader(fh)]
    return rows[:limit] if limit is not None else rows


def read_json(path: Path) -> Any:
    return json.loads(path.read_text(encoding="utf-8"))


def write_json(name: str, payload: Any) -> None:
    for out_dir in (WEB_DATA, BACKEND_DATA):
        path = out_dir / name
        path.write_text(json.dumps(payload, indent=2, sort_keys=False), encoding="utf-8")


def parse_report_timestamp() -> str:
    match = re.search(r"Generated by `report.py` at ([^.]+ UTC)\.", REPORT.read_text(encoding="utf-8"))
    if not match:
        raise RuntimeError(f"Could not parse report timestamp from {REPORT}")
    return match.group(1)


def numeric(row: dict[str, Any], key: str, default: float = 0.0) -> float:
    value = row.get(key)
    if isinstance(value, (int, float)):
        return float(value)
    return default


def classification_metrics(rows: list[dict[str, Any]], threshold: float = 0.5) -> dict[str, Any]:
    if not rows:
        return {
            "n": 0,
            "coverage": 0,
            "accuracy": None,
            "precision_up": None,
            "recall_up": None,
            "f1_binary": None,
            "f1_macro": None,
            "pos_rate": None,
        }

    tp = fp = tn = fn = 0
    for row in rows:
        target = int(row["target"])
        pred = 1 if float(row["proba_up"]) >= threshold else 0
        if pred == 1 and target == 1:
            tp += 1
        elif pred == 1 and target == 0:
            fp += 1
        elif pred == 0 and target == 0:
            tn += 1
        else:
            fn += 1

    def safe_div(num: float, den: float) -> float | None:
        return num / den if den else None

    precision_up = safe_div(tp, tp + fp)
    recall_up = safe_div(tp, tp + fn)
    precision_down = safe_div(tn, tn + fn)
    recall_down = safe_div(tn, tn + fp)

    def f1(precision: float | None, recall: float | None) -> float | None:
        if precision is None or recall is None or precision + recall == 0:
            return None
        return 2 * precision * recall / (precision + recall)

    f1_up = f1(precision_up, recall_up)
    f1_down = f1(precision_down, recall_down)
    f1_values = [value for value in (f1_up, f1_down) if value is not None]
    n = len(rows)

    return {
        "n": n,
        "coverage": n,
        "accuracy": safe_div(tp + tn, n),
        "precision_up": precision_up,
        "recall_up": recall_up,
        "precision_down": precision_down,
        "recall_down": recall_down,
        "f1_binary": f1_up,
        "f1_macro": mean(f1_values) if f1_values else None,
        "pos_rate": safe_div(tp + fp, n),
        "tp": tp,
        "fp": fp,
        "tn": tn,
        "fn": fn,
    }


def dataset_summary(path: Path) -> dict[str, Any]:
    with path.open(newline="", encoding="utf-8") as fh:
        reader = csv.reader(fh)
        header = next(reader)
        rows = list(reader)
    dates = [row[0] for row in rows if row]
    return {
        "file": str(path.relative_to(ROOT)),
        "rows": len(rows),
        "columns": len(header),
        "date_min": min(dates) if dates else None,
        "date_max": max(dates) if dates else None,
    }


def feature_group(feature: str, leakage_rows: dict[str, dict[str, Any]]) -> str:
    if feature in leakage_rows:
        return str(leakage_rows[feature].get("group") or "Other")
    if "gdelt" in feature:
        return "GDELT"
    if "conflict" in feature or "fatal" in feature:
        return "Conflict"
    if "inventory" in feature or "production" in feature or "import" in feature:
        return "Supply"
    if "oil" in feature or "sp500" in feature or "vix" in feature or "usd" in feature:
        return "Market"
    if "month" in feature or "day_of_week" in feature:
        return "Calendar"
    if "yield" in feature or "rate" in feature or "cpi" in feature:
        return "Macro"
    return "Other"


def copy_assets() -> list[dict[str, Any]]:
    asset_groups = {
        "eda-current": ROOT / "assets" / "eda_current",
        "eda-legacy": ROOT / "assets" / "eda_legacy_presentation",
        "ml-legacy": ROOT / "assets" / "ml_legacy_step5c",
    }
    index: list[dict[str, Any]] = []
    for group, source_dir in asset_groups.items():
        target_dir = WEB_ASSETS / group
        target_dir.mkdir(parents=True, exist_ok=True)
        for image in sorted(source_dir.glob("*.png")):
            target = target_dir / image.name
            shutil.copy2(image, target)
            index.append(
                {
                    "group": group,
                    "name": image.stem.replace("_", " ").replace("-", " ").title(),
                    "file": image.name,
                    "path": f"/assets/{group}/{image.name}",
                    "source": str(image.relative_to(ROOT)),
                }
            )
    return index


def build_live_prediction_payloads(
    decision_log: list[dict[str, Any]],
) -> tuple[dict[str, Any], dict[str, Any]]:
    dataset_rows = read_csv(DATASET_DIR / "dataset_final_noleak_step5c_scaler.csv")
    if not dataset_rows:
        raise RuntimeError("No rows found in scaled dataset")

    excluded = {"date", "oil_return_fwd1", "oil_return_fwd1_date"}
    feature_columns = [key for key in dataset_rows[0].keys() if key not in excluded]
    by_target_date = {
        str(row.get("oil_return_fwd1_date")): row
        for row in dataset_rows
        if row.get("oil_return_fwd1_date")
    }
    by_source_date = {
        str(row.get("date")): row
        for row in dataset_rows
        if row.get("date")
    }

    reference_rows: list[dict[str, Any]] = []
    for decision in decision_log:
        target_date = str(decision["date"])
        dataset_row = by_target_date.get(target_date) or by_source_date.get(target_date)
        if not dataset_row:
            continue
        features = {
            feature: float(dataset_row[feature])
            for feature in feature_columns
            if isinstance(dataset_row.get(feature), (int, float))
        }
        reference_rows.append(
            {
                "id": f"hist-{target_date}",
                "source_date": dataset_row.get("date"),
                "target_date": target_date,
                "features": features,
                "actual": {
                    "target": decision["target"],
                    "label": decision["actual_label"],
                },
                "prediction": {
                    "model": "ENS_FINAL3",
                    "proba_up": decision["proba_up"],
                    "pred": decision["pred"],
                    "label": decision["pred_label"],
                    "correct": decision["correct"],
                    "confidence_margin": decision["confidence_margin"],
                },
            }
        )

    if len(reference_rows) != len(decision_log):
        raise RuntimeError(
            f"Live reference row count {len(reference_rows)} does not match decision log {len(decision_log)}"
        )

    stats: dict[str, dict[str, float]] = {}
    for feature in feature_columns:
        values = [float(row["features"][feature]) for row in reference_rows if feature in row["features"]]
        avg = mean(values)
        variance = mean([(value - avg) ** 2 for value in values]) if len(values) > 1 else 0.0
        stats[feature] = {
            "mean": avg,
            "std": math.sqrt(variance) or 1.0,
            "min": min(values),
            "max": max(values),
        }

    def pick_example(
        label: str,
        description: str,
        predicate: Any,
        reverse: bool = True,
    ) -> dict[str, Any] | None:
        candidates = [row for row in reference_rows if predicate(row)]
        if not candidates:
            return None
        row = sorted(
            candidates,
            key=lambda item: float(item["prediction"]["confidence_margin"]),
            reverse=reverse,
        )[0]
        return {
            **row,
            "label": label,
            "description": description,
        }

    picked = [
        pick_example(
            "High-confidence UP hit",
            "A strong historical UP prediction that was correct.",
            lambda row: row["prediction"]["pred"] == 1 and row["prediction"]["correct"],
        ),
        pick_example(
            "High-confidence DOWN hit",
            "A strong historical DOWN prediction that was correct.",
            lambda row: row["prediction"]["pred"] == 0 and row["prediction"]["correct"],
        ),
        pick_example(
            "Low-confidence coin flip",
            "A near-threshold case useful for stress-testing edits.",
            lambda row: True,
            reverse=False,
        ),
        pick_example(
            "False UP signal",
            "A predicted UP day that actually closed DOWN.",
            lambda row: row["prediction"]["pred"] == 1 and not row["prediction"]["correct"],
        ),
        pick_example(
            "False DOWN signal",
            "A predicted DOWN day that actually closed UP.",
            lambda row: row["prediction"]["pred"] == 0 and not row["prediction"]["correct"],
        ),
    ]

    for row in reversed(reference_rows):
        if row["actual"]["label"] == "UP":
            picked.append({**row, "label": "Recent actual UP", "description": "Recent test-period UP example."})
            break
    for row in reversed(reference_rows):
        if row["actual"]["label"] == "DOWN":
            picked.append({**row, "label": "Recent actual DOWN", "description": "Recent test-period DOWN example."})
            break

    examples: list[dict[str, Any]] = []
    seen_ids: set[str] = set()
    for row in picked:
        if row is None or row["id"] in seen_ids:
            continue
        seen_ids.add(row["id"])
        examples.append(row)

    examples_payload = {
        "mode": "historical_examples_for_live_demo",
        "model": "ENS_FINAL3",
        "feature_columns": feature_columns,
        "feature_stats": stats,
        "rows": examples,
        "note": "Selecting an example replays a validated historical prediction. Editing fields uses the backend nearest-neighbor demo scorer.",
    }

    reference_payload = {
        "mode": "nearest_neighbor_demo_reference",
        "model": "ENS_FINAL3",
        "feature_columns": feature_columns,
        "feature_stats": stats,
        "rows": reference_rows,
        "note": "This reference set supports the live demo scorer. It is not a replacement for production model inference.",
    }

    return examples_payload, reference_payload


def build() -> None:
    ensure_dirs()

    leaderboard = read_csv(RESULTS / "primary_test_leaderboard.csv")
    for idx, row in enumerate(leaderboard, start=1):
        row["rank"] = idx

    best = leaderboard[0]
    best_by_experiment: dict[str, dict[str, Any]] = {}
    for row in leaderboard:
        experiment = str(row.get("Experiment") or "unknown")
        existing = best_by_experiment.get(experiment)
        if existing is None:
            best_by_experiment[experiment] = row
            continue
        current_key = (
            numeric(row, "F1_macro"),
            numeric(row, "Accuracy"),
            numeric(row, "AUC"),
        )
        existing_key = (
            numeric(existing, "F1_macro"),
            numeric(existing, "Accuracy"),
            numeric(existing, "AUC"),
        )
        if current_key > existing_key:
            best_by_experiment[experiment] = row

    leakage_rows_raw = read_csv(EDA_TABLES / "step5_upgraded_leakage_risk_assessment.csv")
    leakage_by_feature = {str(row["feature"]): row for row in leakage_rows_raw if row.get("feature")}

    selected_features_payload = read_json(RESULTS / "selected_features.json")
    selected_features = set(selected_features_payload.get("selected_features", []))
    feature_rows = read_csv(RESULTS / "feature_ranking.csv")
    for idx, row in enumerate(feature_rows, start=1):
        feature = str(row["feature"])
        row["rank"] = idx
        row["group"] = feature_group(feature, leakage_by_feature)
        row["selected"] = feature in selected_features
        row["risk"] = leakage_by_feature.get(feature, {}).get("risk", "unknown")
        row["why"] = leakage_by_feature.get(feature, {}).get("why", "")

    top_models = [str(row["ModelConfig"]) for row in leaderboard[:10] if row.get("ModelConfig")]
    featured_models = list(OrderedDict.fromkeys(["ENS_FINAL3", "XGB_linear03", "LGBM_exp100", *top_models]))

    prediction_rows: list[dict[str, Any]] = []
    for path in PREDICTION_FILES:
        if path.exists():
            prediction_rows.extend(read_csv(path))

    wide_by_date: OrderedDict[str, dict[str, Any]] = OrderedDict()
    model_dates: defaultdict[str, set[str]] = defaultdict(set)
    for row in sorted(prediction_rows, key=lambda item: (str(item.get("date")), str(item.get("Model")))):
        model = str(row.get("Model"))
        if model not in featured_models:
            continue
        date = str(row["date"])
        target = int(row["target"])
        entry = wide_by_date.setdefault(date, {"date": date, "target": target, "models": {}})
        entry["models"][model] = {
            "experiment": row.get("Experiment"),
            "proba_up": row.get("proba_up"),
            "pred_05": row.get("pred_05"),
            "pred_val_threshold": row.get("pred_val_threshold"),
            "threshold": row.get("threshold"),
        }
        model_dates[model].add(date)

    ens_rows_all = [
        row
        for row in prediction_rows
        if row.get("Model") == "ENS_FINAL3" and row.get("Split") == "test"
    ]
    ens_rows_all.sort(key=lambda row: str(row.get("date")))
    ens_rows = []
    seen_ens_dates = set()
    for row in ens_rows_all:
        date = str(row.get("date"))
        if date in seen_ens_dates:
            continue
        seen_ens_dates.add(date)
        ens_rows.append(row)
    decision_log = []
    for row in ens_rows:
        proba = float(row["proba_up"])
        pred = 1 if proba >= 0.5 else 0
        target = int(row["target"])
        decision_log.append(
            {
                "date": row["date"],
                "target": target,
                "actual_label": "UP" if target == 1 else "DOWN",
                "proba_up": proba,
                "pred": pred,
                "pred_label": "UP" if pred == 1 else "DOWN",
                "correct": pred == target,
                "confidence_margin": abs(proba - 0.5),
                "val_threshold": row.get("threshold"),
                "val_threshold_pred": row.get("pred_val_threshold"),
            }
        )

    live_examples, live_reference = build_live_prediction_payloads(decision_log)

    threshold_curve = []
    for step in range(35, 66, 1):
        threshold = step / 100
        threshold_curve.append({"threshold": threshold, **classification_metrics(ens_rows, threshold)})

    confidence_curve = []
    total = len(ens_rows)
    for step in range(0, 46, 5):
        min_margin = step / 100
        covered = [row for row in ens_rows if abs(float(row["proba_up"]) - 0.5) >= min_margin]
        metrics = classification_metrics(covered)
        metrics["coverage_rate"] = len(covered) / total if total else 0
        metrics["min_margin"] = min_margin
        confidence_curve.append(metrics)

    dataset_files = [
        DATASET_DIR / "dataset_step4_transformed.csv",
        DATASET_DIR / "dataset_step4_noleak.csv",
        DATASET_DIR / "dataset_final_noleak.csv",
        DATASET_DIR / "dataset_final_noleak_processed.csv",
        DATASET_DIR / "dataset_final_noleak_step5c_scaler.csv",
    ]
    dataset_evolution = [dataset_summary(path) for path in dataset_files if path.exists()]

    asset_index = copy_assets()
    source_hashes = {
        str(path.relative_to(ROOT)): sha256(path)
        for path in SOURCE_FILES
        if path.exists()
    }

    report_timestamp = parse_report_timestamp()
    manifest = {
        "app_name": "Oil Direction Signal Mine",
        "data_version": report_timestamp.replace(" ", "_").replace(":", ""),
        "generated_from": "demo/scripts/build_demo_data.py",
        "report_timestamp": report_timestamp,
        "problem": "Predict whether next trading day oil return is UP or DOWN.",
        "target": "oil_return_fwd1 > 0",
        "split_contract": {
            "train": "target date < 2022-01-01",
            "validation": "2022-01-01 <= target date < 2023-01-01",
            "test": "target date >= 2023-01-01",
        },
        "best_model": {
            "model": best.get("Model"),
            "model_config": best.get("ModelConfig"),
            "experiment": best.get("Experiment"),
            "accuracy": best.get("Accuracy"),
            "f1_macro": best.get("F1_macro"),
            "auc": best.get("AUC"),
            "n": best.get("N"),
            "coverage": best.get("Coverage"),
        },
        "counts": {
            "leaderboard_rows": len(leaderboard),
            "test_days": len(ens_rows),
            "wide_prediction_days": len(wide_by_date),
            "feature_rows": len(feature_rows),
            "assets": len(asset_index),
            "live_examples": len(live_examples["rows"]),
        },
        "source_hashes": source_hashes,
        "local_control_status": {
            "github_repo": "trungdangtapcode/cs313-oil-prediction",
            "gcp_project": "husky-car",
            "gcp_cli_account": "tcuong1000@gmail.com",
            "ci_note": "GitHub Actions requires a separate deploy identity; local gcloud auth is not inherited.",
        },
    }

    feature_groups: defaultdict[str, dict[str, Any]] = defaultdict(
        lambda: {"group": "", "total": 0, "selected": 0, "low_risk": 0, "features": []}
    )
    for row in feature_rows:
        group = str(row["group"])
        summary = feature_groups[group]
        summary["group"] = group
        summary["total"] += 1
        summary["selected"] += 1 if row.get("selected") else 0
        summary["low_risk"] += 1 if row.get("risk") == "low" else 0
        if len(summary["features"]) < 6:
            summary["features"].append(row["feature"])

    mlops_status = {
        "report_timestamp": report_timestamp,
        "artifact_status": read_csv(RESULTS / "experiment_artifact_status.csv")
        if (RESULTS / "experiment_artifact_status.csv").exists()
        else [],
        "metric_contract": read_json(RESULTS / "metric_contract.json"),
        "source_hashes": source_hashes,
        "validation_rules": [
            "Best primary row must remain ENS_FINAL3_th05.",
            "Report timestamp must match the generated data manifest.",
            "Frontend and backend data bundles must be byte-identical JSON payloads.",
            "CI must fail if required source artifacts are absent.",
        ],
    }

    write_json("demo_manifest.json", manifest)
    write_json("leaderboard.json", {"rows": leaderboard, "best_by_experiment": list(best_by_experiment.values())})
    write_json("prediction_wide_test.json", {"models": featured_models, "rows": list(wide_by_date.values())})
    write_json("ens_final3_decision_log.json", {"model": "ENS_FINAL3", "rows": decision_log})
    write_json("feature_ranking.json", {"selected": selected_features_payload, "rows": feature_rows})
    write_json("feature_groups.json", {"rows": sorted(feature_groups.values(), key=lambda row: row["group"])})
    write_json("threshold_diagnostics.json", {"rows": read_csv(RESULTS / "threshold_diagnostics.csv")})
    write_json("selective_coverage_diagnostics.json", {"rows": read_csv(RESULTS / "selective_coverage_diagnostics.csv")})
    write_json("threshold_curve.json", {"model": "ENS_FINAL3", "rows": threshold_curve})
    write_json("confidence_curve.json", {"model": "ENS_FINAL3", "rows": confidence_curve})
    write_json("dataset_evolution.json", {"rows": dataset_evolution})
    write_json("leakage_audit_table.json", {"rows": leakage_rows_raw})
    write_json("asset_index.json", {"rows": asset_index})
    write_json("mlops_status.json", mlops_status)
    write_json("live_prediction_examples.json", live_examples)
    write_json("live_prediction_reference.json", live_reference)

    print(
        json.dumps(
            {
                "report_timestamp": report_timestamp,
                "best_model": manifest["best_model"],
                "json_files": len(list(WEB_DATA.glob("*.json"))),
                "assets": len(asset_index),
                "prediction_models": {model: len(dates) for model, dates in sorted(model_dates.items())},
            },
            indent=2,
        )
    )


if __name__ == "__main__":
    build()
