#!/usr/bin/env python3
"""Validate generated demo JSON against the authoritative staged ML artifacts."""

from __future__ import annotations

import csv
import hashlib
import json
import re
import sys
from pathlib import Path
from typing import Any


ROOT = Path(__file__).resolve().parents[1]
WEB_DATA = ROOT / "web" / "public" / "data"
BACKEND_DATA = ROOT / "backend" / "data"
REPORT = ROOT / "artifacts" / "improve_classification" / "REPORT.md"
LEADERBOARD = ROOT / "artifacts" / "improve_classification" / "results" / "primary_test_leaderboard.csv"

EXPECTED = {
    "report_timestamp": "2026-05-14 12:08:11 UTC",
    "model": "ENS_FINAL3_th05",
    "model_config": "ENS_FINAL3",
    "accuracy": 0.5476190476190477,
    "f1_macro": 0.5405766396462786,
    "auc": 0.535119843987392,
    "n": 840,
}


def fail(message: str) -> None:
    print(f"VALIDATION FAILED: {message}", file=sys.stderr)
    raise SystemExit(1)


def load_json(path: Path) -> Any:
    if not path.exists():
        fail(f"missing {path}")
    return json.loads(path.read_text(encoding="utf-8"))


def sha256(path: Path) -> str:
    h = hashlib.sha256()
    with path.open("rb") as fh:
        for block in iter(lambda: fh.read(1024 * 1024), b""):
            h.update(block)
    return h.hexdigest()


def parse_report_timestamp() -> str:
    text = REPORT.read_text(encoding="utf-8")
    match = re.search(r"Generated by `report.py` at ([^.]+ UTC)\.", text)
    if not match:
        fail("could not parse report timestamp")
    return match.group(1)


def read_first_leaderboard_row() -> dict[str, str]:
    with LEADERBOARD.open(newline="", encoding="utf-8") as fh:
        reader = csv.DictReader(fh)
        try:
            return next(reader)
        except StopIteration:
            fail("leaderboard has no rows")


def assert_close(name: str, actual: Any, expected: float, tolerance: float = 1e-12) -> None:
    try:
        value = float(actual)
    except (TypeError, ValueError):
        fail(f"{name} is not numeric: {actual!r}")
    if abs(value - expected) > tolerance:
        fail(f"{name} expected {expected}, got {value}")


def main() -> None:
    manifest = load_json(WEB_DATA / "demo_manifest.json")
    backend_manifest = load_json(BACKEND_DATA / "demo_manifest.json")
    if manifest != backend_manifest:
        fail("frontend and backend manifests differ")

    timestamp = parse_report_timestamp()
    if timestamp != EXPECTED["report_timestamp"]:
        fail(f"unexpected report timestamp {timestamp}")
    if manifest.get("report_timestamp") != EXPECTED["report_timestamp"]:
        fail("manifest report timestamp does not match expected latest report")

    first = read_first_leaderboard_row()
    if first.get("Model") != EXPECTED["model"]:
        fail(f"expected best model {EXPECTED['model']}, got {first.get('Model')}")
    if first.get("ModelConfig") != EXPECTED["model_config"]:
        fail(f"expected best config {EXPECTED['model_config']}, got {first.get('ModelConfig')}")

    best = manifest.get("best_model", {})
    if best.get("model") != EXPECTED["model"]:
        fail("manifest best model is stale")
    if best.get("model_config") != EXPECTED["model_config"]:
        fail("manifest best model config is stale")
    assert_close("accuracy", best.get("accuracy"), EXPECTED["accuracy"])
    assert_close("f1_macro", best.get("f1_macro"), EXPECTED["f1_macro"])
    assert_close("auc", best.get("auc"), EXPECTED["auc"])
    if int(best.get("n", 0)) != EXPECTED["n"]:
        fail(f"expected n={EXPECTED['n']}, got {best.get('n')}")

    source_hashes = manifest.get("source_hashes", {})
    if not source_hashes:
        fail("manifest has no source hashes")
    for relative_path, expected_hash in source_hashes.items():
        source = ROOT / relative_path
        if not source.exists():
            fail(f"source hash target missing: {relative_path}")
        actual_hash = sha256(source)
        if actual_hash != expected_hash:
            fail(f"source hash mismatch for {relative_path}")

    required_files = [
        "leaderboard.json",
        "prediction_wide_test.json",
        "ens_final3_decision_log.json",
        "feature_ranking.json",
        "confidence_curve.json",
        "threshold_curve.json",
        "dataset_evolution.json",
        "leakage_audit_table.json",
        "asset_index.json",
        "mlops_status.json",
    ]
    for name in required_files:
        web_payload = load_json(WEB_DATA / name)
        backend_payload = load_json(BACKEND_DATA / name)
        if web_payload != backend_payload:
            fail(f"frontend/backend data mismatch for {name}")

    predictions = load_json(WEB_DATA / "prediction_wide_test.json")
    if len(predictions.get("rows", [])) != EXPECTED["n"]:
        fail("wide prediction row count must match final test rows")
    if "ENS_FINAL3" not in predictions.get("models", []):
        fail("ENS_FINAL3 missing from prediction bundle")

    print(
        json.dumps(
            {
                "status": "ok",
                "report_timestamp": manifest["report_timestamp"],
                "best_model": best,
                "validated_files": len(required_files) + 1,
            },
            indent=2,
        )
    )


if __name__ == "__main__":
    main()
